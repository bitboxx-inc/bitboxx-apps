    <h2 class="text-2xl font-semibold mt-6 mb-2">2. 収集する情報</h2>
    <p>本サービスでは、アプリの機能によって以下の情報を収集する場合があります。<br>
        ただし、個々のアプリで情報を収集しない場合は、この項目は適用されません。</p>

    <h3 class="text-xl font-semibold mt-4"> (1) ユーザーが提供する情報</h3>
    <ul class="list-disc pl-6 mt-2">
        <li>名前、メールアドレス、電話番号（ユーザー登録が必要な場合）</li>
        <li>アプリ内でユーザーが入力するデータ（例: コメント、フィードバック、カスタム設定）</li>
    </ul>

    <h3 class="text-xl font-semibold mt-4"> (2) 自動的に収集される情報</h3>
    <ul class="list-disc pl-6 mt-2">
        <li>デバイス情報（モデル、OSバージョン、アプリのバージョン）</li>
        <li>利用状況（アプリの操作ログ、エラーログ）</li>
        <li>位置情報（特定のアプリで必要な場合のみ）</li>
    </ul>

    <h3 class="text-xl font-semibold mt-4"> (3) サードパーティによるデータ収集</h3>
    <p>本サービスは、以下の外部サービスを利用する場合があります。</p>
    <ul class="list-disc pl-6 mt-2">
        <li><strong>分析ツール:</strong> Google Analytics、Firebase Analytics など</li>
        <li><strong>広告:</strong> AdMob、Facebook Ads、Apple Search Ads など</li>
        <li><strong>認証:</strong> Google、Apple、Facebook など</li>
    </ul>

